public class BitWiseOperators {
    public static void main(String[] args) {
        //BitWise AND
        System.out.println(5 & 6);

        //BitWise OR
        System.out.println(5 | 6);

        //BitWise XOR
        System.out.println(5 ^ 6);

        //Binary One's Complement
        System.out.println(~5);

        //Binary left shift
        System.out.println(1 << 2);

        //Binary right shift
        System.out.println(6 >> 1);
    }
}
